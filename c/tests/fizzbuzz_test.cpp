// trick to capture output sent to printf
int printf(const char *fmt, ...) {
    va_list argp;
    va_start(argp, fmt);
    const int ret = vsnprintf(next, sizeof buffer-(next-buffer), fmt, argp);
    next += ret;
    va_end(argp);
    return ret;
}